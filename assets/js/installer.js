const Installer = (() => {
  const BASE_URL = 'https://raw.githubusercontent.com/gianga82/avm-installer/main';

  function generateCommand(state) {
    let cmd = `curl -fsSL ${BASE_URL}/install.sh | bash -s --`;

    const flags = [];

    if (state.modules.docker) flags.push('--docker');
    if (state.modules.compose) flags.push('--docker-compose');
    if (state.modules.avm !== false) flags.push('--avm');
    if (state.modules.mcp) flags.push('--mcp');
    if (state.modules.opencode) flags.push('--opencode');
    if (state.modules.ollama) flags.push('--ollama');
    if (state.modules.openwebui) flags.push('--openwebui');
    if (state.modules.npm) flags.push('--nginx-proxy-manager');
    if (state.modules.tailscale) flags.push('--tailscale');
    if (state.modules.watchtower) flags.push('--watchtower');
    if (state.modules.monitoring) flags.push('--monitoring');

    if (state.hostname) flags.push(`--hostname ${state.hostname}`);
    if (state.sshPort && state.sshPort !== '22') flags.push(`--ssh-port ${state.sshPort}`);
    if (state.username && state.username !== 'root') flags.push(`--user ${state.username}`);
    if (state.autoUpdate) flags.push('--auto-update');

    if (flags.length > 0) {
      cmd += ' \\\n  ' + flags.join(' \\\n  ');
    }

    return cmd;
  }

  function copyToClipboard(text) {
    if (navigator.clipboard && navigator.clipboard.writeText) {
      navigator.clipboard.writeText(text).catch(() => fallbackCopy(text));
    } else {
      fallbackCopy(text);
    }
  }

  function fallbackCopy(text) {
    const ta = document.createElement('textarea');
    ta.value = text;
    ta.style.position = 'fixed';
    ta.style.opacity = '0';
    document.body.appendChild(ta);
    ta.select();
    try { document.execCommand('copy'); } catch (e) { /* ignore */ }
    document.body.removeChild(ta);
  }

  function downloadScript(command) {
    const blob = new Blob([
      '#!/usr/bin/env bash\n# AI VPS Manager Installer\n# Generated by AVM Installer\n\nset -euo pipefail\n\n' + command.replace(/\\\n\s*/g, ' ') + '\n'
    ], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'install-avm.sh';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  }

  return { generateCommand, copyToClipboard, downloadScript };
})();
